// Helper Functions

// Get a location by ID
export function getLocationById(id) {
  return locations.find(location => location.id === id);
}

// Get all location IDs (for static path generation)
export function getAllLocationIds() {
  return locations.map(location => location.id);
}

// Group locations by region (for organized display)
export function getLocationsByRegion() {
  return locations.reduce((grouped, location) => {
    const region = location.region;
    if (!grouped[region]) {
      grouped[region] = [];
    }
    grouped[region].push(location);
    return grouped;
  }, {});
}

// Group locations by country (alternative organization)
export function getLocationsByCountry() {
  return locations.reduce((grouped, location) => {
    const country = location.country;
    if (!grouped[country]) {
      grouped[country] = [];
    }
    grouped[country].push(location);
    return grouped;
  }, {});
}

// Get count of total locations
export function getLocationCount() {
  return locations.length;
}
